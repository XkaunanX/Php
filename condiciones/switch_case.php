<?php
// Uso de switch en PHP

$dia = 3;
switch ($dia) {
    case 1:
        echo "Lunes<br>";
        break;
    case 2:
        echo "Martes<br>";
        break;
    case 3:
        echo "Miércoles<br>";
        break;
    case 4:
        echo "Jueves<br>";
        break;
    case 5:
        echo "Viernes<br>";
        break;
    case 6:
        echo "Sábado<br>";
        break;
    case 7:
        echo "Domingo<br>";
        break;
    default:
        echo "Día inválido<br>";
        break;
}
?>
