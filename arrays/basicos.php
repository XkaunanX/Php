<?php
// Arreglos simples en PHP (indexados y asociativos)

// Arreglo indexado
$frutas = array("Manzana", "Banana", "Cereza");
echo "Primer fruta: " . $frutas[0] . "<br>"; // Manzana

// Arreglo asociativo
$persona = array(
    "nombre" => "Juan",
    "edad" => 30,
    "ciudad" => "Madrid"
);
echo "Nombre: " . $persona["nombre"] . "<br>"; // Juan
echo "Edad: " . $persona["edad"] . "<br>";   // 30
?>
